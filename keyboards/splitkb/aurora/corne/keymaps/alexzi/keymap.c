bool oled_task_user(void) {
    if (!oled_enabled) {
        oled_off();
        return false;
    }

    // If OLED is enabled, render the logo or other information
    if (is_keyboard_master()) {
        render_my_logo();
    } else {
        render_my_logo2();
    }
    return true;
}

