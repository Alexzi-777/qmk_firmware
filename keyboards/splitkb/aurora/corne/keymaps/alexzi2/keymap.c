static bool oled_enabled = true;

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case EMAIL:
            if (record->event.pressed) {
                send_string("alex@greetabl.com");
            }
            return false;
        case PASS:
            if (record->event.pressed) {
                send_string("Heaven2Is0Not7Far1Away");
            }
            return false;
        case OLED_TOGGLE:
            if (record->event.pressed) {
                oled_enabled = !oled_enabled;
                if (!oled_enabled) {
                    oled_clear();
                    oled_off();
                } else {
                    oled_on();
                }
            }
            return false;
        default:
            return true;
    }
}
